#include <iostream>
using namespace std;

int main() {
    system("chcp 1251 > nul"); // Осигурява правилно изобразяване на кирилица

    int число = 42;
    int* указател = &число; // Присвояване на адреса на променливата `число`

    cout << "Стойност на числото: " << число << endl;
    cout << "Адрес на числото: " << указател << endl;
    cout << "Стойност чрез указател: " << *указател << endl; // Дереференция

    return 0;
}